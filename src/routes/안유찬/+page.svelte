<h2>음식이란?</h2>
<h4>인간이 먹음으로써 활동에 필요한 영양분을 얻을 수 있도록 만들어진 사물, 혹은 인간이 먹고 마시는 행위 자체.</h4>
<h2>1. 인도네시아의 렌당</h2>
<img src="https://edgio.clien.net/F01/6466588/370447145e208c.jpg?scale=width[740],options[limit]" alt="">
<h3>세계음식순위 그 영광의 1위를 차지한 음식은 바로 인도네시아의 대표 음식 렌당입니다. 마치 한국의 갈비찜을 보는 듯한 비주얼이 아주 먹음직스러워 보이죠? 소고리를 코코넛밀크와 레몬그라스, 마늘, 고추 등의 양념을 하여 만든 스튜 형식의 고기찜입니다.</h3>
<h3> CNN 표현에 의하면 "살이 찐 이유"가 바로 렌당이라고 할 정도로 최고의 맛을 자랑하는 음식이라고 하는데요. 부드러워서 남녀노소 즐기기 좋은 인도네시아 음식입니다.</h3>
<h2>2. 일본의 스시 </h2>
<img src="https://mblogthumb-phinf.pstatic.net/MjAxNzEwMjRfMTY3/MDAxNTA4ODA3NjQ0MDM1.23QVfejAuksBAD8xUBHc3TeYvd0ef0HQA2zsndgnTSwg.Jwl1QR103KYrhjN7w5ZuLNlSGJ98KdLKyKrahpAtXN8g.JPEG.dymg98/%EC%82%AC%EC%A7%84_1_%EC%99%84%EC%84%B1_1.jpg?type=w800" alt="">
<h3>스시는 아마 회를 못드시는 분들도 초밥은 드실 수 있는 분들이 많지 않을까 생각하는데요. 싱싱한 해산물을 밥 위에 올려 먹는 독특한 요리이지요. </h3>
<h3>스시는 전 세계인들에게 사랑받고 있는 요리 이기 때문에 한국에서 뿐만 아니라 각국에서 심심치 않게 스시 요리점을 만나볼 수 있는데요. 외국에서는 점심, 저녁 때 테이크아웃으로도 많이 찾는다고 합니다.</h3>
<h2>3. 태국의 똠얌꿍</h2>
<img src="https://img1.daumcdn.net/thumb/C500x500/?fname=http://t1.daumcdn.net/brunch/service/user/2AuX/image/zj2n7NZx6z1p7K_VwXmBQqqPsXY.png" alt="">
<h3>샥스핀, 부야베스 등과 함께 세계 3대 스프라고 불리는 태국의 톰양꿍입니다. 누구든 똠양꿍을 처음 접했을 때는 독특한 맛 때문에 "어떻게 이 음식이 세계 3대 스프가 될 수 있지?"하고 의문을 품을 수 있지만 똠양꿍 특유의 시고 매콤하고 달콤한 맛의 조화에 빠지면 쉽게 헤어나오질 못한답니다.</h3>
<h3>하지만 이렇게 독특한 맛 때문에 아직까지도 호불호가 갈리고 있는 음식이기도 합니다.</h3>
<h2>4. 홍콩의 딤섬</h2>
<img src="https://i.namu.wiki/i/DyuNGLUA9E3yzZbfhNFWARjYw4Jhf27QwQh1bel5h5E3CdNlesymQrDaimVDz8BmvENEDN8HO9QhGA_qZmYrtQ.webp" alt="">
<h3>세계 미식가들의 칭찬을 끊임없이 받고 있는 대표적인 요리가 바로 딤섬이랍니다. 딤섬은 홍콩의 대표 메뉴로 한국에서도 이제는 심심치 않게 찾아볼 수 있는 인기 요리인데요. 고기부터 해산물까지 각종 소를 채워 넣는 딤섬은 그 수를 헤아릴 수도 없을 정도로 많다고 하는데요. </h3>
<h3>모양은 한국의 만두와 비슷하다고 생각하시면 될 것 같네요. 먹기 전에 충분히 식히지 않으면 입 천장을 데일 수 있다는 것! 명심하세요.</h3>
<h2>5. 대한민국의 김치</h2>
<img src="https://i.namu.wiki/i/3u7KQ8jVXWJMJpioMNBo5bFb7NrMd1jUbJrGXy99vSgh57D2w0BHG3RlEnawqyRlfAuTihp37sPnlCKX3IlKNg.webp" alt="">
<h3>한국의 가장 대표적인 요리를 꼽으라면 단연 '김치'를 꼽을 수 있는데요. 맛은 물론이고 몸에도 좋은 유산균 식품으로 알려져 있는 김치입니다. . 한국인이라면 안좋아할 수 없는 대표 식품이라고 할 수 있는데요. </h3>
<h3>배추를 고추가루와 각종 양념으로 발효시켜 먹는 저장음식입니다. 특히 한식당에서는 무료 반찬으로 제공되 외국 관광객들이 많이 신기해 한다고 하네요.</h3>
